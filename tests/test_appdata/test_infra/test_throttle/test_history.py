import inspect
import logging
from datetime import datetime

import numpy as np
import pandas as pd
import pytest

from appstorestream.infra.web.throttle import AThrottleHistory

# ------------------------------------------------------------------------------------------------ #
# pylint: disable=missing-class-docstring, line-too-long
# ------------------------------------------------------------------------------------------------ #
# ------------------------------------------------------------------------------------------------ #
logger = logging.getLogger(__name__)
# ------------------------------------------------------------------------------------------------ #
double_line = f"\n{100 * '='}"
single_line = f"\n{100 * '-'}"


@pytest.mark.athrottle
@pytest.mark.history
class TestAThrottleHistory:  # pragma: no cover
    # ============================================================================================ #
    def test_throttle_history_plot(self, plt, caplog):
        start = datetime.now()
        logger.info(
            f"\n\nStarted {self.__class__.__name__} {inspect.stack()[0][3]} at {start.strftime('%I:%M:%S %p')} on {start.strftime('%m/%d/%Y')}"
        )
        logger.info(double_line)
        # ---------------------------------------------------------------------------------------- #
        history = AThrottleHistory(baseline_latency=0.5)
        for i in range(1, 101):
            history.update_history(
                mean_latency=np.random.random(1)[0],
                delay=np.random.randint(0, 5, 1)[0],
            )
        history.plot_latency(with_delay=True)

        # ---------------------------------------------------------------------------------------- #
        end = datetime.now()
        duration = round((end - start).total_seconds(), 1)

        logger.info(
            f"\n\nCompleted {self.__class__.__name__} {inspect.stack()[0][3]} in {duration} seconds at {start.strftime('%I:%M:%S %p')} on {start.strftime('%m/%d/%Y')}"
        )
        logger.info(single_line)
